function i18nLang() {
  const lang = document.documentElement.getAttribute("data-lang");
  return I18N_SUPPORTED.includes(lang) ? lang : "pt-BR";
}

function i18nLocale() {
  return I18N_LOCALE_MAP[i18nLang()] || "pt-BR";
}

// t("some.key", {name: "x"}) -> looks up the key in the active language (falling
// back to pt-BR, then the key itself) and replaces any {placeholder} tokens.
function t(key, vars) {
  const lang = i18nLang();
  const str = (I18N[lang] && I18N[lang][key]) || (I18N["pt-BR"] && I18N["pt-BR"][key]) || key;
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (m, name) => (name in vars ? vars[name] : m));
}

function applyI18n(root) {
  const scope = root || document;
  scope.querySelectorAll("[data-i18n]").forEach((el) => { el.textContent = t(el.getAttribute("data-i18n")); });
  scope.querySelectorAll("[data-i18n-placeholder]").forEach((el) => { el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder"))); });
  scope.querySelectorAll("[data-i18n-title]").forEach((el) => { el.setAttribute("title", t(el.getAttribute("data-i18n-title"))); });
  scope.querySelectorAll("[data-i18n-html]").forEach((el) => { el.innerHTML = t(el.getAttribute("data-i18n-html")); });
}

// Persists the chosen language as a global setting (admin-only, same as the
// other entries in the Settings panel) and reloads so every server-rendered
// page picks it up via the `lang` template variable.
async function setLanguage(lang) {
  if (!I18N_SUPPORTED.includes(lang)) return;
  await api("/api/settings", { method: "PUT", body: JSON.stringify({ language: lang }) });
  window.location.reload();
}

document.addEventListener("DOMContentLoaded", () => applyI18n());
